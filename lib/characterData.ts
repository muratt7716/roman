export function pick<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)]
}

export function generateCharacter() {
  const isMale = Math.random() > 0.5
  const firstName = pick(isMale ? MALE_NAMES : FEMALE_NAMES)
  const lastName = pick(SURNAMES)
  const age = pick(AGES)
  const personality = pick(PERSONALITIES)
  const background = pick(BACKGROUNDS)
  const motivation = pick(MOTIVATIONS)
  const flaw = pick(FLAWS)
  const appearance = pick(APPEARANCES)

  return { firstName, lastName, age, personality, background, motivation, flaw, appearance, isMale }
}

export type GeneratedCharacter = ReturnType<typeof generateCharacter>
